package data;

public class Year {

	int	year;
	int	yearId;


	public Year(int year, int yearId) {

		super();
		this.year = year;
		this.yearId = yearId;
	}


	public int getYear() {

		return year;
	}


	public void setYear(int year) {

		this.year = year;
	}


	public int getYearId() {

		return yearId;
	}


	public void setYearId(int yearId) {

		this.yearId = yearId;
	}

}
